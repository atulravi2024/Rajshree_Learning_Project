async def generate_tables():
    for n in range(2, 21):
        lines = []
        for i in range(1, 11):
            res = n * i
            lines.append(f"{n} {get_multiplier_word(i)} {res}")
        
        table_text = ", ".join(lines)
        text = f"नमस्ते बच्चों! आज हम सीखेंगे {n} का पहाड़ा। यह बहुत ही मज़ेदार और आसान है। चलिए मेरे साथ बोलिए! {table_text}। वाह! आपने तो कमाल कर दिया! आपने {n} का पहाड़ा बहुत ही अच्छे से याद कर लिया है। शाबाश!"
