async def generate_audio(rel_path, text):
    full_path = os.path.join(AUDIO_BASE_DIR, rel_path.replace('/', os.sep))
    os.makedirs(os.path.dirname(full_path), exist_ok=True)
    
    # Check if we should skip (if it's already > 80KB we might skip, but user wants us to generate)
    # Actually, we'll just overwrite to ensure quality.
    
    print(f"Generating: {rel_path}...")
    try:
        communicate = edge_tts.Communicate(text, VOICE, rate=RATE, pitch=PITCH)
        await communicate.save(full_path)
        size_kb = os.path.getsize(full_path) / 1024
        print(f"  ✅ Success ({size_kb:.1f} KB)")
    except Exception as e:
        print(f"  ❌ Error for {rel_path}: {e}")

